# Create full CLIENT folder structure
# ------------------------------------------------------------
def ensure_client_folder(client_code: str):
    """
    Ensures clients/<client_code>/ exists and contains:
    - client_config.json
    - parser stubs
    - templates
    - output folders
    """

    root = BASE_PATH / client_code
    _ensure(root)

    # Python package init
    _write_if_missing(root / "__init__.py", "# Auto-created client package\n")

    # -----------------------------
    # client_config.json
    # -----------------------------
    config_path = root / "client_config.json"
    if not config_path.exists():
        config_path.write_text(json.dumps({
            "document_prefix": f"{client_code.upper()}_",
            "display_name": client_code,
            "parsers": {
                "nfr": "nfr_parser",
                "raids": "raids_parser",
                "actions": "actions_parser",
            },
            "templates": {
                "nfr_template": f"clients/{client_code}/templates/nfr_template.docx",
                "raid_template": f"clients/{client_code}/templates/raid_template.docx",
                "action_template": f"clients/{client_code}/templates/action_template.docx"
            },
            "dashboard": "dashboards",
            "created": datetime.utcnow().isoformat()
        }, indent=4))

    # -----------------------------
    # Parsers
    # -----------------------------
    _write_if_missing(root / "nfr_parser.py", PARSER_STUB)
    _write_if_missing(root / "raids_parser.py", RAID_PARSER_STUB)
    _write_if_missing(root / "actions_parser.py", ACTION_PARSER_STUB)

    # -----------------------------
    # Dashboard settings
    # -----------------------------
    _write_if_missing(root / "dashboards.py", DASHBOARD_STUB)

    # -----------------------------
    # Templates
    # -----------------------------
    templates = root / "templates"
    _ensure(templates)
    _write_if_missing(templates / "nfr_template.docx", PLACEHOLDER_DOCX)
    _write_if_missing(templates / "raid_template.docx", PLACEHOLDER_DOCX)
    _write_if_missing(templates / "action_template.docx", PLACEHOLDER_DOCX)

    # -----------------------------
    # Output folders for PMO tools
    # -----------------------------
    _ensure(root / "nfr" / "outputs")
    _ensure(root / "raids" / "outputs")
    _ensure(root / "actions" / "outputs")


