# NEW: SAFE NAME HELPERS
# ============================================================

def safe_fs_name(text: str) -> str:
    """
    Converts any string into a filesystem-safe, ASCII-only name:
    - Removes accents
    - Replaces spaces with underscores
    - Keeps alphanumeric + underscores only
    """
    if not text:
        return ""

    # Normalize accents → ASCII
    text = unicodedata.normalize("NFKD", text)
    text = text.encode("ascii", "ignore").decode()

    # Lowercase
    text = text.lower().strip()

    # Replace spaces/dashes with underscores
    text = re.sub(r"[\s\-]+", "_", text)

    # Remove illegal characters
    text = re.sub(r"[^a-z0-9_]+", "", text)

    return text


def normalise_client_name(text: str) -> str:
    """
    Canonical internal identifier for loaders, profiles, NFR generator.
    Allows wider unicode but still cleans dangerous characters.
    """
    if not text:
        return ""

    text = text.strip().lower()
    text = text.replace(" ", "_")
    text = re.sub(r"[^a-z0-9_]+", "", text)

    return text


# ------------------------------------------------------------
# Helper — ensure folder
# ------------------------------------------------------------
def _ensure(path: Path):
    path.mkdir(parents=True, exist_ok=True)


# ------------------------------------------------------------
# Write a file if it does not exist
# ------------------------------------------------------------
def _write_if_missing(path: Path, content: str | bytes):
    if not path.exists():
        if isinstance(content, bytes):
            path.write_bytes(content)
        else:
            path.write_text(content)


