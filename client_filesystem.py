def ensure_project_folder(client_code: str, project_code: str, metadata: dict, settings: dict):
    """
    Creates a COMPLETE PMO folder structure for a project:
    """

    root = BASE_PATH / client_code / project_code
    _ensure(root)

    # -----------------------------
    # 1. Core JSON Files
    # -----------------------------
    (root / "metadata.json").write_text(json.dumps(metadata, indent=4))
    (root / "settings.json").write_text(json.dumps(settings, indent=4))

    # -----------------------------
    # 2. RAIDs Folder
    # -----------------------------
    raids_path = root / "raids"
    _ensure(raids_path)
    _write_if_missing(raids_path / "log.json", "[]")

    # -----------------------------
    # 3. Actions Folder
    # -----------------------------
    actions_path = root / "actions"
    _ensure(actions_path)
    _write_if_missing(actions_path / "log.json", "[]")

    # -----------------------------
    # 4. NFR Folder
    # -----------------------------
    nfr_path = root / "nfr" / "history"
    _ensure(nfr_path)

    # -----------------------------
    # 5. Project Dashboards (optional)
    # -----------------------------
    dashboards_path = root / "dashboards"
    _ensure(dashboards_path)

    _write_if_missing(
        dashboards_path / "project_dashboard.json",
        json.dumps({"layout": "default", "created": datetime.utcnow().isoformat()}, indent=4)
    )

    return root


